def subimg(img, i, j, mrow, ncol):
    'extract a sub matrix m x n @ (i, j)'
    return [[img[irow][jcol] for jcol in range(j, j + ncol)] for irow in range(i, i + mrow)]

def match_update(img, p, i, j):
    'check whether pattern p at i, j matches matrix img, update img if match'
    mrow, ncol =len(p), len(p[0])
    if subimg(img, i, j, mrow, ncol) == p:
        for ix in range(mrow):
            for jx in range(ncol):
                if img[i + ix][j + jx] == 1:
                    img[i + ix][j + jx] = 3
                else:
                    img[i+ix][j+jx] = 2
    # note that original img is modified in the step above
    return img


def checkio(pattern, image):
    pmrow, pncol =len(pattern), len(pattern[0])
    imrow, incol =len(image), len(image[0])
    for ix in range(imrow - pmrow + 1):
        for jx in range(incol - pncol + 1):
            match_update(image, pattern, ix, jx)

    return image

